class NovelGenerator:
    """
    –ö–ª–∞—Å—Å –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–π –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —Ä–æ–º–∞–Ω–∞ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º Gemini API.
    –†–µ–∞–ª–∏–∑—É–µ—Ç DAG-–ø–∞–π–ø–ª–∞–π–Ω: –§—É–Ω–¥–∞–º–µ–Ω—Ç -> –ü–ª–∞–Ω -> –ß–µ—Ä–Ω–æ–≤–∏–∫ -> –†–µ–¥–∞–∫—Ç—É—Ä–∞.
    """
    def __init__(self, api_key, model_name="gemini-2.5-pro"):
        genai.configure(api_key=api_key)
        self.model = genai.GenerativeModel(model_name)
        self.world_bible = {}
        self.scenes = []

    def _call_gemini(self, prompt_text, attempt_count=3):
        """–ù–∞–¥–µ–∂–Ω—ã–π –≤—ã–∑–æ–≤ API —Å –ø–æ–≤—Ç–æ—Ä–Ω—ã–º–∏ –ø–æ–ø—ã—Ç–∫–∞–º–∏."""
        print(f"  > –û—Ç–ø—Ä–∞–≤–∫–∞ –∑–∞–ø—Ä–æ—Å–∞ –≤ Gemini...")
        try:
            print(prompt_text)
            response = self.model.generate_content(prompt_text)
            # –î–æ–±–∞–≤–ª–µ–Ω–∞ –∑–∞–¥–µ—Ä–∂–∫–∞, —á—Ç–æ–±—ã –Ω–µ –ø—Ä–µ–≤—ã—à–∞—Ç—å –ª–∏–º–∏—Ç—ã –∑–∞–ø—Ä–æ—Å–æ–≤
            time.sleep(5)
            print(response.text)
            return response.text
        except Exception as e:
            print(f"   ! –û—à–∏–±–∫–∞ API: {e}. –ü–æ–ø—ã—Ç–∫–∞ {attempt_count}...")
            if attempt_count > 0:
                time.sleep(10)
                return self._call_gemini(prompt_text, attempt_count - 1)
            else:
                print("   ! –ù–µ —É–¥–∞–ª–æ—Å—å –≤—ã–ø–æ–ª–Ω–∏—Ç—å –∑–∞–ø—Ä–æ—Å –∫ API –ø–æ—Å–ª–µ –Ω–µ—Å–∫–æ–ª—å–∫–∏—Ö –ø–æ–ø—ã—Ç–æ–∫.")
                return None

    def _extract_scenes(self, scene_plan_text):
        """–ò–∑–≤–ª–µ–∫–∞–µ—Ç –ø—Ä–æ–Ω—É–º–µ—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å—Ü–µ–Ω –∏–∑ —Ç–µ–∫—Å—Ç–∞."""
        # –ò—â–µ–º –ø—Ä–æ–Ω—É–º–µ—Ä–æ–≤–∞–Ω–Ω—ã–µ –ø—É–Ω–∫—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ —è–≤–ª—è—é—Ç—Å—è —Å—Ü–µ–Ω–∞–º–∏
        # –≠—Ç–æ—Ç –ø–∞—Ç—Ç–µ—Ä–Ω –∏—â–µ—Ç —Å—Ç—Ä–æ–∫–∏, –Ω–∞—á–∏–Ω–∞—é—â–∏–µ—Å—è —Å —á–∏—Å–ª–∞ –∏ —Ç–æ—á–∫–∏.
        scenes = re.findall(r'^\d+\.\s.*', scene_plan_text, re.MULTILINE)
        if not scenes: # –ï—Å–ª–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ, –ø—Ä–æ–±—É–µ–º –∏—Å–∫–∞—Ç—å –ø—É–Ω–∫—Ç—ã-–º–∞—Ä–∫–µ—Ä—ã
            scenes = re.findall(r'^[\*\-]\s.*', scene_plan_text, re.MULTILINE)

        print(f"  > –ù–∞–π–¥–µ–Ω–æ {len(scenes)} —Å—Ü–µ–Ω.")
        return scenes if scenes else [scene_plan_text] # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –≤–µ—Å—å —Ç–µ–∫—Å—Ç –µ—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏ —Å—Ü–µ–Ω

